//===================================//
//中断服务程序  AD采样中断在ADC程序里
//===================================//
void int_isr(void) __interrupt 
{    	
	__asm__("org 0x08");			//中断入口地址			
	PUSH(_A_BUFF,_R3_BUFF);			//中断入栈保护
//=========Tcc中断程序===============//
	if(TCIF )					//判断TCIF是否为1
	{
		TCC	  += 0x06;				//1/2 * 8 * (256-6) = 1000us	公式：1/IRC频率 * 预分频 * （256-初值）
		ISR    = 0xfe;				//清TC0中断标志位
	//	io_ledOut = !io_ledOut;			//P61口翻转
	}	
	if(EXIF)					//判断EXIF是否为1,外部中断
	{	 
		ISR    = 0xfb;				//清EXIF中断标志位
//		PORT6_4 = !PORT6_4;			//P64口翻转
	}
	if(ICIF)					//判断ICIF是否为1,端口变化中断
	{	 
		ISR    = 0xfd;				//清ICIF中断标志位
//		PORT6_4 = !PORT6_4;			//P64口翻转
	}
	
	if(T1IF)					//判断T1IF是否为1,pwm周期中断
	{	 
		ISR    = 0xf7;				//ISR的bit3:T1IF清零
//		PORT6_4 = !PORT6_4;			//P64口翻转
	}
//===============中断程序===============//
	POP(_A_BUFF,_R3_BUFF);			//中断出栈保护恢复
}

